y = 5   # Assigns 5 to y

x = 10
x += 5  # Equivalent to x = x + 5, so x becomes 15
y = 10
y -= 3  # Equivalent to y = y - 3, so y becomes 7
z = 4
z *= 2  # Equivalent to z = z * 2, so z becomes 8
w = 8
w /= 4  # Equivalent to w = w / 4, so w becomes 2.0

a = 10
b = 5
print(a == b)  # Output: False
print(a != b)  # Output: True
print(a > b)   # Output: True
print(a < b)   # Output: False
print(a >= b)  # Output: True
print(a <= b)  # Output: False